package com.dodo.practice.operator;

public class Demo01 {
    public static void main(String[] args) {
        // = 赋值运算符
        Long a = 100000000000000L;
        int b = 123;
        short c = 11;
        byte d = 6;

        System.out.println(a+b+c+d);    //Long
        System.out.println(b+c+d);  //Int
        System.out.println(c+d);    //Int


    }
}